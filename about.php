		<p>The VIVEK SAINDANE'S College, Nashik Road was founded on 2nd June 1963, by the premier education society in Maharashtra, the Gokhale Education Society which was founded by visionary educationist Late Prin. T. A. Kulkarni who was disciple of Namdar Gopal Krishna Gokhale. The College is situated in Nashik Road region of the Nashik City. The Nashik Road region has number of organizations like Security Press, currency Note Press and Artillery Center at Deolali Camp. The Nashik Road area has a peculiar social structure as it is adjoined by number of villages on the periphery of Nashik City. At the time of establishment of the college, there were no Colleges in the nearby towns like Manmad, Lasalgaon, Sinnar, Igatpuri etc. As a result in the initial period the College provided higher education facilities not only in Nashik Road area but also to the adjoining Talukas of Nashik district with the rural bias and high proportion of population of backward class communities. The college caters the needs of higher education of the neediest Segments of the society. Taking this Socio-cultural background into consideration, the college was started as multi faculty educational institution with Arts, Commerce and Science faculties.

			The college provides flexibility in proving opportunities of learning by selecting number of subjects as special subjects at the Graduation and Post-Graduate level with the help of generous donors and agencies like UGC. The college has successfully developed Physical recourses like classrooms, Library and reading rooms, Gymnasium, Playgrounds, Seminar hall etc.

			The social structure being region-specific, the peculiar feature of the college has been that it is catering to the educational needs of the students who had been first generation learners. Along with academic programmes the college has developed excellent ties with the social and business organizations, N.G.O.s in and around the Nashik City and Nashik Road region. With the activities like SDO, N.S.S., N.C.C. Literacy Associations, Dramatic Association, Debating Association the college has made all out efforts for the personality development of the students. Over the period the departments of the College have created excellent ties with the national level organizations like IUCA, IIT Powai, etc. for development of teaching faculty. Continuous efforts were made to make use of the schemes like FIP of the UGC under which many tree faculty members have undergone M.Phil and Ph. D. programmes. This has immensely helped in increasing the effectiveness of teaching programmes.
	
